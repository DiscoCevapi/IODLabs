// Brands
let brands = ["Apple", "Samsung", "Google", "Microsoft", "Amazon"]
console.log(brands)

// 1. Replacing the value of the element of position  0 and 3
brands  [0] = "OnePlus"
brands  [3] = "Facebook"
console.log(brands)


// 1. Add a new brand to the start of the array
brands.unshift("Ebay")
console.log(brands)

// 2. Remove the last brand from the array
brands.pop()
console.log(brands)
